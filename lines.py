s1 = input("Введите сроку s1: ")
s2 = input("Введите сроку s2: ")
print("Длина строки s1:", len(s1))
print("Длина строки s2:", len(s2))
print("Первый символ строки s1:", s1[:1])
print("Последний символ строки s2:", s2[-1:])
print("Строка s1 равна сроке s2:", s1 == s2)
print("Строка s1 седержится в сроке s2:", s1 in s2)
print("Строка s2 седержится в сроке s1:", s2 in s1)
